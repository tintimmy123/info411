# ╔═╡ 9424823d-9442-45fc-b33e-3e11fe936c02
function rand_fore_regr(X, y, sr1, sr2)
	Random.seed!(1)
	DecisionTreeRegressor = @load DecisionTreeRegressor pkg=DecisionTree 								verbosity=0
	tree_reg = DecisionTreeRegressor()
	forest_reg = EnsembleModel(model=tree_reg, n=200)
	r1 = range(forest_reg, :(model.n_subfeatures), lower=1, upper=6)
	r2 = range(forest_reg, :bagging_fraction, lower=0.4, upper=1.0)
	r3 = range(forest_reg, :(model.max_depth), lower = 2, upper = 20)
	tr = [r1,r2,r3]
	tm = TunedModel(model=forest_reg, tuning=Grid(resolution=10),  									resampling=CV(nfolds=10), ranges=[tr[sr1],tr[sr2]], 
					measure=rms)
	m = machine(tm, X, y)
	MLJ.fit!(m)
return m
end

