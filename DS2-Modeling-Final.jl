	r1 = range(forest, :(model.n_subfeatures), lower=1, upper=6)
	r2 = range(forest, :bagging_fraction, lower=0.4, upper=1.0)
	r3 = range(forest, :(model.max_depth), lower = 2, upper = 20)
	tr = [r1,r2,r3]
	tm = TunedModel(model=forest, tuning=Grid(resolution=10),  										resampling=CV(nfolds=10), ranges=[tr[sr1],tr[sr2]], 
					measure=meas)
	m = machine(tm, X, y)
	MLJ.fit!(m)
return m
end

