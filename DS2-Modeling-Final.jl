Since the Random Foreset Classification function has been created in the DS1 section, now function `rand_fore_regr()` is created to use Random Forest Regression model. Similarly to the classification model, here three parameters (number of subfeatures, bagging fraction, max depth of the tree) have been added to the function, but only 2 out of 3 can be applied to train the model:
